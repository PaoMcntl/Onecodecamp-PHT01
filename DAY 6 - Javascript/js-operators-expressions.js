//Arithmetic Operations
let x = 10;
let y = 5; 

//Sum or Total '+'
let sum = x + y;
console.log ("The sum is: ",sum);

//Difference '-'
let difference = x - y;
console.log ("The difference is: ",difference);

//Product '*'
let product = x * y;
console.log ("The product is: ",product);

//Quotient '/'
let qoutient = x / y;
console.log ("The quotient is: ",qoutient);

//Remainder or Modulo
let remainder = x % 3;
console.log ("The remainder:",remainder);

let answer = 10 + 2 * 23 / 2;
console.log ("The answer is",answer);

//Concatenation
let a = "Hello";
let b = "World!";
console.log (a +" "+ b);

//Assignment expressions
let i = 40;
let j = 20;

//Assignment expressions '+=' 
i += j;
console.log (i);

//Assignment expressions '+=' 
i -= j;
console.log (i);

//Assignment expressions '*=' 
i *= j;
console.log (i);

//Assignment expressions '/=' 
i /= j;
console.log (i);